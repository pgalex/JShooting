package com.jshooting.shootingDatabase;

/**
 * Training method
 *
 * @author pgalex
 */
public class TrainingMethod
{
	/**
	 * Id
	 */
	private long id;
	/**
	 * Name
	 */
	private String name;

	/**
	 * Id
	 *
	 * @return the id
	 */
	public long getId()
	{
		return id;
	}

	/**
	 * Id
	 *
	 * @param id the id to set
	 */
	public void setId(long id)
	{
		this.id = id;
	}

	/**
	 * Name
	 *
	 * @return the name
	 */
	public String getName()
	{
		return name;
	}

	/**
	 * Name
	 *
	 * @param name the name to set
	 */
	public void setName(String name)
	{
		this.name = name;
	}
}
